function quadsim_mat2csv(mat_filename)
    data = load(mat_filename);
    variableNames = {'P','Q','R','Phi','The','Psi','U','V','W','X','Y','Z', ...
        'RPM1','RPM2','RPM3','RPM4','RPM5','RPM6',...
        'THR1','THR2','THR3','THR4','THR5','THR6',...
        'Phi_sp','The_sp','Psi_sp','Alt_sp',...
        'X_sp','Y_sp',...
        'Ax','Ay','Az',...
        'M1 Status','M2 Status','M3 Status','M4 Status','M5 Status','M6 Status'};
    
    T = array2table(data.yout);
    T.Properties.VariableNames = variableNames;
    outputFilename = strcat(mat_filename,'.csv');
    
    writetable(T,outputFilename);
    fprintf('MAT file "%s" has been converted to CSV file "%s".\n', mat_filename, outputFilename);
end